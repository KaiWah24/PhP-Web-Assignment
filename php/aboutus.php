        <div class="story">
            <h1>OUR STORY</h1>
                <div class="story0 container d-flex justify-content-between">
                    <div class="story1">
                        <p>Founded in 2017, KW Esports Gaming Society is on a mission to unite the esports industry’s fragmented data to power more engaging, live experiences for fantasy players, fans, streamers, teams and event broadcasters around the world.</p>

                        <p>Starting with League of Legends, we built a computer vision and machine learning-powered platform capable of capturing and analyzing thousands of data points in real-time. This enabled us to generate one of the most comprehensive databases of League of Legends esports stats on the planet, going all the way back to the first professional game played in 2011 and being updated with new match data in real-time.</p>
                    </div>

                    <div class="story2">
                        <p>In 2018, we partnered and began working closely with Riot Games by providing real-time statistics and storylines to their on-air broadcast team, leveling up their ability to tell engaging stories to their viewers. Since then, we have expanded our platform’s capabilities to include Overwatch, with more titles in development to be supported in the near future.</p>

                        <p>In 2020, we are bringing our database of stats to the masses with Esports One Fantasy. Users will research teams and players, set their lineup, and compete with friends in the first all-in-one fantasy platform built specifically for esports. We are extremely excited to launch for the start of the 2020 Spring Split. </p>
                    </div>
